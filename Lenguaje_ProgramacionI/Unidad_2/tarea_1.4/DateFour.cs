using System;
class DateThree{
  private int day;
  private int month;
  private int year;

  public int getDay()
  {
    
    return day;
  }

  public void setDay(int d)
  {
    day = d;
  }
  public int getMonth()
  {
    return month;
  }

public void setMonth(int m)
  {
    month = m;
  }
  public int getYear()
  {
    return year;
  }

  public void setYear(int y)
  {
    year = y;
  }


}